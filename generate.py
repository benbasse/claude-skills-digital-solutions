def main():
    index_by_cat = {}
    for p in PROFILES:
        folder = os.path.join(SKILLS_DIR, p["category"], p["key"])
        os.makedirs(folder, exist_ok=True)
        content = render_skill(p)
        with open(os.path.join(folder, "SKILL.md"), "w", encoding="utf-8") as f:
            f.write(content)
        index_by_cat.setdefault(p["category"], []).append(p)
        print(f"Created: {p['category']}/{p['key']}/SKILL.md")
    print(f"\nTotal: {len(PROFILES)} skills generated in {SKILLS_DIR}")
    return index_by_cat


if __name__ == "__main__":
    main()
