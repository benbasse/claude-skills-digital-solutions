"""
Generator for the Claude Skills library.

Single source of truth: every SKILL.md under skills/ is generated from the
PROFILES list below. Edit a profile here and re-run this script rather than
hand-editing the generated Markdown, so the whole library stays consistent.
"""
import os

ROOT = os.path.dirname(os.path.abspath(__file__))
SKILLS_DIR = os.path.join(ROOT, "skills")


def section(title, items):
    if not items:
        return ""
    body = "\n".join(f"- {it}" for it in items)
    return f"## {title}\n\n{body}\n\n"


def render_skill(p):
    fm_desc = p["description"].replace('"', "'")
    out = []
    out.append("---\n")
    out.append(f"name: {p['name']}\n")
    out.append(f"description: {fm_desc}\n")
    out.append("---\n\n")
    out.append(f"# {p['title']}\n\n")
    out.append(f"{p['mission']}\n\n")
    out.append(section("When to trigger this skill", p["triggers"]))
    out.append(section("Skills, responsibilities and best practices", p["competencies"]))
    out.append(section("Common pitfalls to avoid", p["pitfalls"]))
    out.append(section("Reference stack and tools", p["stack"]))
    out.append(section("Typical deliverables", p["deliverables"]))
    out.append(section("Example prompts that should trigger this skill", p["example_prompts"]))
    if p.get("checklist"):
        out.append("## Quick checklist before delivering\n\n")
        out.append("\n".join(f"- [ ] {c}" for c in p["checklist"]))
        out.append("\n\n")
    if p.get("notes"):
        out.append(f"## Notes\n\n{p['notes']}\n")
    return "".join(out)


PROFILES = []


def add(key, category, name, title, description, mission, triggers, competencies,
        pitfalls, stack, deliverables, example_prompts, checklist=None, notes=None):
    PROFILES.append(dict(key=key, category=category, name=name, title=title, description=description,
                          mission=mission, triggers=triggers, competencies=competencies,
                          pitfalls=pitfalls, stack=stack,
                          deliverables=deliverables, example_prompts=example_prompts,
                          checklist=checklist, notes=notes))

